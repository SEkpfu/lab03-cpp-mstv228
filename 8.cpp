#include <iostream>
using namespace std;

int main() {
    int n;

    // Ввод размера доски
    cout << "Введите размер доски n x n (четное число): ";
    cin >> n;

    // Проверка на четность
    if (n % 2 != 0 || n <= 0) {
        cout << "Ошибка: размер должен быть четным положительным числом!" << endl;
        return 1;
    }

    // Вывод шахматной доски
    for (int i = 0; i < n; i++) {
        for (int j = 0; j < n; j++) {
            // Если сумма индексов четная - белая клетка, иначе - черная
            if ((i + j) % 2 == 0) {
                cout << "o ";  // белая клетка
            }
            else {
                cout << "* ";  // черная клетка
            }
        }
        cout << endl;  // переход на новую строку после каждой строки доски
    }

    return 0;
}